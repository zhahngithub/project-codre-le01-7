public class ScientificNumber {
    private double num;
    private String sign;

    public ScientificNumber(double num, String sign) {
        this.num = num;
        this.sign = sign;
    }

    public double getNum() {
        return num;
    }

    public void setNum(double num) {
        this.num = num;
    }

    public String getSign() {
        return sign;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }

    
}
